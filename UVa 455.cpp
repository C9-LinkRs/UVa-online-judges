vector<int> z_function(string s){
    int n = (int) s.length();
    vector<int> z(n);
    for (int i = 1, l = 0, r = 0; i < n; ++i) {
        if (i <= r)
            z[i] = min (r - i + 1, z[i - l]);
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }
    return z;
}

int string_compression(string s){
    vector<int> z=z_function(s);
    int n=(int)s.length(), ans=0;
    for(int i = 1; i < z.size(); i++){
        if(n%i == 0 && i+z[i] == n){
            ans=i;
            break;
        }
    }
    return (ans == 0)?n:ans;
}

